                <h2>La rétine</h2>
                <p>La rétine est la partie sensible de l’œil, fine membrane composée de cellules appelées cônes et bâtonnets qui captent la lumière (photon) pour la transformer en signaux électrochimiques transmis au cerveau. La partie centrale de la rétine appelée aussi macula, est composée principalement de cônes et elle qui intervient dans la vision précise et la vision des couleurs. La partie périphérique de la rétine est composée de bâtonnets responsables de la vision périphérique et de la vision nocturne. L’examen du fond d’œil permet de visualiser la rétine en détails et de dépister ou d’évaluer les signes d’hypertension artérielle et de diabète, <span id="text-sousligne">la dégénérescence maculaire liée à l’âge</span>, des thromboses vasculaires…. Un examen préventif régulier du fond d’œil est recommandé, en particulier si vous faites partie de groupes dits à risque (histoire familiale, diabète, hypertension artérielle, tabagisme, obésité entre autres). La dégénérescence maculaire est causée par la destruction des cellules rétiniennes dans la région centrale appelée la macula;
Il existe 2 formes :l'une sèche et l'autre humide.
Les premiers signes sont   souvent un besoin accru de lumière ,une déformation des lignes qui apparaissent courbes et plus tard des taches sombres dans le champs de vision.
La dégénérescence humide est plus rare. Elle est la conséquence de l'apparition de vaisseaux anormaux dans la macula. Du sang et du liquide  séreux peuvent  alors se répandre et obscurcir la vision.</p>
