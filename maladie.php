                <h2>Le glaucome</h2>
                <p>Le glaucome est une maladie oculaire traduisant une augmentation plus ou moins sévère de la tension intraoculaire.
                    <br/> La maladie peut passer inaperçue car elle est souvent asymptomatique, même si parfois des maux de tête ou des troubles visuels sont signalés. Elle génère une destruction progressive des fibres du nerf optique et peut conduire à une perte sévère de la vision voire une cécité si elle n'est pas diagnostiquée à temps.
                    <br/> Il est donc recommandé de pratiquer des examens réguliers de la tension intraoculaires dès l'âge de 40 ans, voire même plus tôt, s'il existe des antécédents familiaux de glaucome, et aussi si vous êtes atteints d’hypertension artérielle ou de diabète.
                    <br/> En général et si le diagnostic est précoce, la simple instillation de collyres suffit à diminuer la tension intraoculaire. Dans la minorité des cas, un traitement par laser ou chirurgical peut s'avérer nécessaire.</p>
